import java.util.Scanner;

public class BOJ11719 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        String text;

        while(in.hasNextLine()) {
            text = in.nextLine();
            System.out.println(text);
        }
    }
}
